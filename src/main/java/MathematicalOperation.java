public class MathematicalOperation {
    public static void main(String[] args) {

        System.out.println(4/2);
        System.out.println(4+2);
        System.out.println(4-2);
        System.out.println(4*2);
        System.out.println(9/3);
        System.out.println(9/2);
        System.out.println(9.0/2);
        System.out.println(9/2.0);
        System.out.println(9.0/2.0);
        System.out.println((float) 9/2);
        System.out.println(0/2);
//        System.out.println(9/0);Give Arithematic exception
//        System.out.println(0/0);
        System.out.println(9.0/0);// floating numbers divided by 0=ifinity
        System.out.println(9/0.0);//will give infinity
        System.out.println(9.0/0.0);//will give infinity
        System.out.println(0.0/0.0);//will give a NAN (Not a number output)
        System.out.println(0.0/2.0);//0.0
        System.out.println((float)9/0);//infinity
        System.out.println(0.0/0);//Nan
        System.out.println(0/0.0);//NaN
        System.out.println(0.0/0.0);//NaN
        System.out.println(9/0.0);
        System.out.println('a'/2);
        System.out.println(9%3);
        System.out.println(9%2);
        System.out.println(100%5);
        System.out.println(8%2);

    }
}
