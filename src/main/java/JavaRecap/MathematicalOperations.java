package JavaRecap;

public class MathematicalOperations {
    public static void main(String[] args) {
        System.out.println(4/2);
        System.out.println(4+2);
        System.out.println(4-2);
        System.out.println(4*2);
        System.out.println(9/3);
        System.out.println(9/2);//eqaul to 4.5 reason is .5 is decimal
        System.out.println(9.0/2);
        System.out.println(9.0/2.0);
        System.out.println((float)9/2);//gives a float result
        System.out.println(0/9);
//        System.out.println(9/0);will give an arthematic exception
//        System.out.println(0/0);//will give a arthmeatic exception
        System.out.println(9.0/0);//will give an infinity
        System.out.println(9/0.0);////will give an infinity
        System.out.println(9.0/0.0);//will give an infinity
        System.out.println(0.0/0.0);//will give a NAN (Not a number)
        System.out.println(0.0/2.0);
        System.out.println((float)9/0);
        System.out.println(0.0/0);//NaN not a number
        System.out.println((9/0.0));//Infinity
        System.out.println(('a'/2));
        System.out.println((byte)('a'));
        System.out.println(9%3);
        System.out.println(100%5);





    }
}
